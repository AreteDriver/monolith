"""
eval/narration_eval.py

LLM Narration Quality Evaluator for Monolith.

Scores the plain_english narration generated by the Anthropic API against
the evidence_json that was available at narration time.

Dimensions scored (0.0–1.0 each):
  - factual_grounding:   Does the narration reference evidence IDs or specific
                         values that actually appear in evidence_json?
  - severity_alignment:  Does the narration's language match the detected severity?
  - actionability:       Does the narration tell someone what to do?
  - hallucination_flag:  Did the LLM invent specific claims not in evidence?

NOTE on the grader approach:
  This script uses a DETERMINISTIC rubric (regex + keyword checks) rather than
  a second LLM call. This is intentional — LLM-grading-LLM adds noise and cost.
  The hallucination check looks for specific numeric values or IDs in the narration
  that do NOT appear in the evidence block. That's the most reliable signal.

Usage:
    python eval/narration_eval.py
    python eval/narration_eval.py --db path/to/monolith.db --limit 50
    python eval/narration_eval.py --json
    python eval/narration_eval.py --report-id MNL-20260324-001
"""

import argparse
import json
import re
import sqlite3
import sys
from dataclasses import asdict, dataclass, field
from pathlib import Path

# ---------------------------------------------------------------------------
# Severity vocabulary — words expected in narration for each severity level
# ---------------------------------------------------------------------------
SEVERITY_VOCAB: dict[str, list[str]] = {
    "CRITICAL": [
        "critical",
        "immediate",
        "urgent",
        "severe",
        "high priority",
        "serious",
        "significant risk",
        "compromised",
    ],
    "HIGH": [
        "high",
        "important",
        "notable",
        "concerning",
        "requires attention",
        "elevated",
        "suspicious",
    ],
    "MEDIUM": [
        "medium",
        "moderate",
        "potential",
        "possible",
        "may indicate",
        "worth reviewing",
        "monitor",
    ],
    "LOW": [
        "low",
        "minor",
        "informational",
        "routine",
        "normal variation",
        "unlikely",
        "background",
    ],
}

# Words that suggest actionability
ACTIONABILITY_VOCAB = [
    "investigate",
    "review",
    "check",
    "report",
    "contact",
    "submit",
    "monitor",
    "escalate",
    "verify",
    "examine",
    "flag",
    "alert",
    "should",
    "recommend",
    "suggest",
    "consider",
    "action",
]

# Minimum narration length to be considered non-trivial
MIN_NARRATION_LENGTH = 50

# ANSI color codes for terminal output
GREEN = "\033[92m"
RED = "\033[91m"
YELLOW = "\033[93m"
RESET = "\033[0m"

# Hallucination detection: numeric patterns that could be fabricated
# We extract these from narration and check they exist in evidence
NUMERIC_PATTERN = re.compile(r"\b(\d{4,})\b")  # 4+ digit numbers (IDs, quantities, etc.)
TX_HASH_PATTERN = re.compile(r"0x[a-fA-F0-9]{10,}")  # Transaction hashes


# ---------------------------------------------------------------------------
# Data Classes
# ---------------------------------------------------------------------------
@dataclass
class NarrationScore:
    report_id: str
    anomaly_id: str
    severity: str
    factual_grounding: float  # 0.0–1.0
    severity_alignment: float  # 0.0–1.0
    actionability: float  # 0.0–1.0
    hallucination_flag: bool  # True = hallucination detected
    hallucination_details: list[str] = field(default_factory=list)
    composite_score: float = 0.0
    narration_length: int = 0
    notes: str = ""

    def __post_init__(self):
        # Composite: average of non-hallucination dims, penalized if hallucination
        base = (self.factual_grounding + self.severity_alignment + self.actionability) / 3
        self.composite_score = round(base * (0.5 if self.hallucination_flag else 1.0), 4)


@dataclass
class NarrationEvalSummary:
    db_path: str
    reports_evaluated: int
    reports_skipped: int
    avg_factual_grounding: float
    avg_severity_alignment: float
    avg_actionability: float
    hallucination_rate: float  # 0.0–1.0
    avg_composite_score: float
    passed: bool  # composite >= 0.70 and hallucination_rate <= 0.10
    per_report: list[NarrationScore] = field(default_factory=list)


# ---------------------------------------------------------------------------
# Scoring Functions
# ---------------------------------------------------------------------------
def score_factual_grounding(narration: str, evidence: dict) -> float:
    """
    Check that specific evidence values appear in the narration.

    Strategy: Extract key identifiable strings from evidence (object IDs,
    system IDs, wallet addresses) and check how many appear in the narration.
    A score of 1.0 means all key evidence tokens were referenced.
    """
    if not evidence:
        return 0.5  # No evidence to check against — neutral

    # Extract identifiable tokens from evidence (IDs, hashes, numeric values)
    evidence_str = json.dumps(evidence)

    # Find potential ID tokens: long alphanumeric strings and tx hashes
    id_tokens = set(re.findall(r"\b[a-zA-Z0-9_]{8,}\b", evidence_str))
    tx_hashes = set(TX_HASH_PATTERN.findall(evidence_str))

    all_tokens = id_tokens | tx_hashes
    if not all_tokens:
        return 0.5  # Nothing identifiable in evidence

    # Check how many appear in narration
    narration_lower = narration.lower()
    found = sum(1 for t in all_tokens if t.lower() in narration_lower)

    # We don't expect ALL tokens — just enough to show grounding
    # Score based on fraction found, capped at 1.0
    grounding = min(found / max(len(all_tokens) * 0.3, 1), 1.0)
    return round(grounding, 4)


def score_severity_alignment(narration: str, severity: str) -> float:
    """
    Check that narration language matches the severity level.
    Returns 1.0 if expected vocabulary present, 0.0 if opposing vocabulary present.
    """
    narration_lower = narration.lower()
    severity = severity.upper()

    expected_words = SEVERITY_VOCAB.get(severity, [])
    if not expected_words:
        return 0.5

    # Check for expected severity words
    expected_hits = sum(1 for w in expected_words if w in narration_lower)

    # Check for mismatched severity words (e.g., "routine" in a CRITICAL alert)
    opposing = {
        "CRITICAL": SEVERITY_VOCAB.get("LOW", []),
        "HIGH": SEVERITY_VOCAB.get("LOW", []),
        "MEDIUM": [],
        "LOW": SEVERITY_VOCAB.get("CRITICAL", []) + SEVERITY_VOCAB.get("HIGH", []),
    }
    opposing_hits = sum(1 for w in opposing.get(severity, []) if w in narration_lower)

    if opposing_hits > 0:
        return max(0.0, 0.3 - (opposing_hits * 0.1))

    if expected_hits == 0:
        return 0.3  # Neutral language — not wrong, but not aligned either

    return min(1.0, 0.6 + (expected_hits * 0.1))


def score_actionability(narration: str) -> float:
    """Check that narration tells someone what to do."""
    narration_lower = narration.lower()
    hits = sum(1 for w in ACTIONABILITY_VOCAB if w in narration_lower)
    return round(min(1.0, hits * 0.25), 4)


def check_hallucination(narration: str, evidence: dict) -> tuple[bool, list[str]]:
    """
    Detect fabricated specifics.

    Strategy: Find all 4+ digit numbers and tx hashes in the narration.
    If any appear that are NOT in the evidence block, flag as hallucination.

    This catches the most dangerous hallucination: fabricated IDs, quantities,
    and transaction hashes that look authoritative but don't exist in evidence.
    """
    evidence_str = json.dumps(evidence)

    # Extract numbers from narration
    narration_numbers = set(NUMERIC_PATTERN.findall(narration))
    narration_hashes = set(TX_HASH_PATTERN.findall(narration))

    fabricated = []

    for num in narration_numbers:
        # Short numbers (years, round numbers) are more likely coincidental — skip
        if len(num) <= 4 and int(num) in range(2020, 2030):
            continue
        if num not in evidence_str:
            fabricated.append(f"number '{num}' not found in evidence")

    for h in narration_hashes:
        if h not in evidence_str:
            fabricated.append(f"tx hash '{h[:16]}...' not found in evidence")

    return len(fabricated) > 0, fabricated


# ---------------------------------------------------------------------------
# Per-Report Scorer
# ---------------------------------------------------------------------------
def score_report(row: sqlite3.Row) -> NarrationScore | None:
    """Score a single bug_reports row. Returns None if unevaluable."""
    plain_english = row["plain_english"] or ""
    evidence_json = row["evidence_json"] or "{}"
    severity = row["severity"] or "MEDIUM"
    report_id = row["report_id"] or str(row["id"])
    anomaly_id = row["anomaly_id"] or ""

    if len(plain_english.strip()) < MIN_NARRATION_LENGTH:
        return None  # Too short to evaluate — probably not LLM-generated

    try:
        evidence = json.loads(evidence_json)
    except json.JSONDecodeError:
        evidence = {}

    factual = score_factual_grounding(plain_english, evidence)
    alignment = score_severity_alignment(plain_english, severity)
    actionable = score_actionability(plain_english)
    hallucinated, details = check_hallucination(plain_english, evidence)

    return NarrationScore(
        report_id=report_id,
        anomaly_id=anomaly_id,
        severity=severity,
        factual_grounding=factual,
        severity_alignment=alignment,
        actionability=actionable,
        hallucination_flag=hallucinated,
        hallucination_details=details,
        narration_length=len(plain_english),
    )


# ---------------------------------------------------------------------------
# Orchestrator
# ---------------------------------------------------------------------------
def run_eval(
    db_path: str,
    limit: int = 100,
    report_id: str | None = None,
) -> NarrationEvalSummary:
    path = Path(db_path)
    if not path.exists():
        raise FileNotFoundError(f"Database not found: {db_path}")

    conn = sqlite3.connect(db_path)
    conn.row_factory = sqlite3.Row

    try:
        if report_id:
            cursor = conn.execute(
                """
                SELECT id, report_id, anomaly_id, severity,
                       plain_english, evidence_json
                FROM bug_reports
                WHERE report_id = ?
                """,
                (report_id,),
            )
        else:
            cursor = conn.execute(
                """
                SELECT id, report_id, anomaly_id, severity,
                       plain_english, evidence_json
                FROM bug_reports
                WHERE plain_english IS NOT NULL
                ORDER BY id DESC
                LIMIT ?
                """,
                (limit,),
            )

        rows = cursor.fetchall()
    finally:
        conn.close()

    scores: list[NarrationScore] = []
    skipped = 0

    for row in rows:
        result = score_report(row)
        if result is None:
            skipped += 1
        else:
            scores.append(result)

    if not scores:
        return NarrationEvalSummary(
            db_path=db_path,
            reports_evaluated=0,
            reports_skipped=skipped,
            avg_factual_grounding=0.0,
            avg_severity_alignment=0.0,
            avg_actionability=0.0,
            hallucination_rate=0.0,
            avg_composite_score=0.0,
            passed=False,
            per_report=[],
        )

    n = len(scores)
    hallucination_rate = sum(1 for s in scores if s.hallucination_flag) / n

    return NarrationEvalSummary(
        db_path=db_path,
        reports_evaluated=n,
        reports_skipped=skipped,
        avg_factual_grounding=round(sum(s.factual_grounding for s in scores) / n, 4),
        avg_severity_alignment=round(sum(s.severity_alignment for s in scores) / n, 4),
        avg_actionability=round(sum(s.actionability for s in scores) / n, 4),
        hallucination_rate=round(hallucination_rate, 4),
        avg_composite_score=round(sum(s.composite_score for s in scores) / n, 4),
        passed=(sum(s.composite_score for s in scores) / n >= 0.70 and hallucination_rate <= 0.10),
        per_report=scores,
    )


# ---------------------------------------------------------------------------
# Reporting
# ---------------------------------------------------------------------------
def print_report(summary: NarrationEvalSummary, verbose: bool = False) -> None:
    def color(val: float, threshold: float, invert: bool = False) -> str:
        passed = val <= threshold if invert else val >= threshold
        c = GREEN if passed else RED
        return f"{c}{val:.4f}{RESET}"

    print(f"\n{'=' * 62}")
    print("  MONOLITH NARRATION QUALITY EVAL")
    print(f"  DB: {summary.db_path}")
    print(f"  Evaluated: {summary.reports_evaluated}  |  Skipped: {summary.reports_skipped}")
    print(f"{'=' * 62}")

    if summary.reports_evaluated == 0:
        print(f"\n  {YELLOW}No evaluable reports found.{RESET}")
        print(f"  Reports need plain_english >= {MIN_NARRATION_LENGTH} chars to be scored.")
        print(f"{'=' * 62}\n")
        return

    overall = f"{GREEN}PASS{RESET}" if summary.passed else f"{RED}FAIL{RESET}"
    print(f"\n  {'Metric':<28} {'Score':>10}  {'Target':>10}")
    print(f"  {'-' * 52}")
    fg = color(summary.avg_factual_grounding, 0.60)
    sa = color(summary.avg_severity_alignment, 0.65)
    ac = color(summary.avg_actionability, 0.50)
    hr = color(summary.hallucination_rate, 0.10, invert=True)
    cs = color(summary.avg_composite_score, 0.70)
    print(f"  {'Factual Grounding':<28} {fg:>10}  {'>=0.60':>10}")
    print(f"  {'Severity Alignment':<28} {sa:>10}  {'>=0.65':>10}")
    print(f"  {'Actionability':<28} {ac:>10}  {'>=0.50':>10}")
    print(f"  {'Hallucination Rate':<28} {hr:>10}  {'<=0.10':>10}")
    print(f"  {'-' * 52}")
    print(f"  {'Composite Score':<28} {cs:>10}  {'>=0.70':>10}  {overall}")
    print(f"{'=' * 62}")

    if verbose and summary.per_report:
        print("\n  PER-REPORT DETAIL (showing flagged reports)")
        flagged = [
            s for s in summary.per_report if s.hallucination_flag or s.composite_score < 0.60
        ]
        if not flagged:
            print(f"  {GREEN}No flagged reports.{RESET}")
        for s in flagged[:10]:
            print(
                f"\n  Report: {s.report_id}  |  Anomaly: {s.anomaly_id}  |  Severity: {s.severity}"
            )
            hall = "YES" if s.hallucination_flag else "no"
            print(f"  Composite: {s.composite_score:.4f}  |  Hallucination: {hall}")
            if s.hallucination_details:
                for d in s.hallucination_details[:3]:
                    print(f"    ⚠  {d}")
    print()


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------
def main() -> None:
    parser = argparse.ArgumentParser(description="Monolith Narration Quality Evaluator")
    parser.add_argument("--db", default="monolith.db")
    parser.add_argument("--limit", type=int, default=100, help="Max reports to evaluate")
    parser.add_argument("--report-id", help="Evaluate a single report by ID")
    parser.add_argument("--json", action="store_true")
    parser.add_argument("--verbose", action="store_true", help="Show per-report detail")
    parser.add_argument(
        "--fail-on-regression",
        action="store_true",
        help="Exit code 1 if eval fails (use in CI)",
    )
    args = parser.parse_args()

    try:
        summary = run_eval(args.db, limit=args.limit, report_id=args.report_id)
    except FileNotFoundError as e:
        print(f"ERROR: {e}", file=sys.stderr)
        sys.exit(1)

    if args.json:
        out = asdict(summary)
        print(json.dumps(out, indent=2))
    else:
        print_report(summary, verbose=args.verbose)

    if args.fail_on_regression and not summary.passed:
        sys.exit(1)


if __name__ == "__main__":
    main()
